package com.hyzx.xschool.web.controller.request;

import org.apache.commons.lang.builder.ToStringBuilder;

/**
 * Created by jack on 15-11-7.
 */
public class TopicStoreFormBean {
  private Long id;
  private String name;
  private String goodDesc;
  private String badDesc;
  private String trainPlanName;
  
  public Long getId() {
    return id;
  }

  public void setId(Long id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getGoodDesc() {
    return goodDesc;
  }

  public void setGoodDesc(String goodDesc) {
    this.goodDesc = goodDesc;
  }

  public String getBadDesc() {
    return badDesc;
  }

  public void setBadDesc(String badDesc) {
    this.badDesc = badDesc;
  }

  public String getTrainPlanName() {
    return trainPlanName;
  }

  public void setTrainPlanName(String trainPlanName) {
    this.trainPlanName = trainPlanName;
  }

  @Override
  public String toString() {
    return new ToStringBuilder(this).toString();
  }
}
