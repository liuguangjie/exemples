public class TaskInfo {
  private Long id;
  private String name;
  private Integer hardType;
  private boolean hasVideo;

  private String tpicStoreName;
  private Long noPlayTimes;
  private Integer status;

  private String createTime;

  public boolean isHasVideo() {
    return hasVideo;
  }

  public void setHasVideo(boolean hasVideo) {
    this.hasVideo = hasVideo;
  }

  public void setId(Long id) {
    this.id = id;
  }

  public Long getId() {
    return id;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getName() {
    return name;
  }

  public Integer getHardType() {
    return hardType;
  }

  public void setHardType(Integer hardType) {
    this.hardType = hardType;
  }

  public void setTpicStoreName(String tpicStoreName) {
    this.tpicStoreName = tpicStoreName;
  }

  public String getTpicStoreName() {
    return tpicStoreName;
  }

  public void setNoPlayTimes(Long noPlayTimes) {
    this.noPlayTimes = noPlayTimes;
  }

  public Long getNoPlayTimes() {
    return noPlayTimes;
  }

  public Integer getStatus() {
    return status;
  }

  public void setStatus(Integer status) {
    this.status = status;
  }

  public String getCreateTime() {
    return createTime;
  }

  public void setCreateTime(String createTime) {
    this.createTime = createTime;
  }
}
